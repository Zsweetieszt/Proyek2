// Fungsi untuk memperbarui status game
void updateGame() {
    if (!isAlive) return;
    if (!hasWon) {  // Jika belum menang, cek tabrakan
        checkCollisionWithFlag();
    }


    playerY += velocityY;
    velocityY += GRAVITY;


    if (playerY >= GROUND_HEIGHT +17) {
        playerY = GROUND_HEIGHT +17;
        isJumping = 0;
    }
    

    // Cek tabrakan dengan musuh, duri, coin, starpower, nextLevel,Block
    cheakCollisionWithBlock();
    checkCollisionWithMonster();
    checkCollisionWithSpike();
    checkCollisionWithCoin();
    checkCollisionWithStar();
    checkCollisionWithNextLevel();

    for (int i = 0; i < totalMonsters; i++) {
        if (monsterX[i] == -9999 && monsterY[i] == -9999) continue;  // Jangan gerakkan monster yang sudah mati

        // **Gerakkan monster lebih jauh dengan kecepatan 3 piksel per frame**
        monsterX[i] += monsterDirection[i] * 3;

        // **Hanya ubah arah jika monster sudah benar-benar melewati batas**
        if (monsterX[i] <= monsterMinX[i] || monsterX[i] >= monsterMaxX[i]) {
            delay(100);  // ✅ Tambahkan delay kecil agar monster tidak langsung balik arah
            monsterDirection[i] *= -1;
        }
    }


    // Untuk Star Power
    if (hasStarPower) {
        starPowerTimer--;
        if (starPowerTimer <= 0) {
            hasStarPower = 0;  // Matikan Star Power setelah 10 detik
        }
    }
    

}
