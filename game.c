void findMarioStartPosition() {
    for (int i = 0; i < MAP_HEIGHT; i++) {
        for (int j = 0; j < TOTAL_MAP_WIDTH; j++) {
            if (maps[level][i][j] == 12) {
                playerX = j * (SCREEN_WIDTH / MAP_WIDTH);
                playerY = i * (SCREEN_HEIGHT / MAP_HEIGHT);

                // Atur kamera agar langsung melihat Mario
                cameraX = playerX - (SCREEN_WIDTH / 2);

                // Pastikan kamera tidak keluar batas level
                if (cameraX < 0) cameraX = 0;
                if (cameraX > TOTAL_MAP_WIDTH * (SCREEN_WIDTH / MAP_WIDTH) - SCREEN_WIDTH)
                    cameraX = TOTAL_MAP_WIDTH * (SCREEN_WIDTH / MAP_WIDTH) - SCREEN_WIDTH;

                return; // Keluar setelah menemukan posisi awal
            }
        }
    }
}

void findAllMonsters() {
    totalMonsters = 0;

    for (int i = 0; i < MAP_HEIGHT; i++) {
        for (int j = 0; j < TOTAL_MAP_WIDTH; j++) {
            if (maps[level][i][j] == 4) {  
                if (totalMonsters < MAX_MONSTERS) {
                    monsterX[totalMonsters] = j * (SCREEN_WIDTH / MAP_WIDTH);
                    monsterY[totalMonsters] = i * (SCREEN_HEIGHT / MAP_HEIGHT);

                    // **Gunakan batas gerakan lebih luas (300 piksel ke kiri & kanan)**
                    monsterMinX[totalMonsters] = monsterX[totalMonsters] - 150;
                    monsterMaxX[totalMonsters] = monsterX[totalMonsters] + 150;

                    // **Berikan arah gerak awal acak**
                    monsterDirection[totalMonsters] = (rand() % 2 == 0) ? 1 : -1;

                    totalMonsters++;
                }
            }
        }
    }
}







