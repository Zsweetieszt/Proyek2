// Fungsi untuk memperbarui status game
void updateGame() {
    if (!isAlive) return;


    playerY += velocityY;
    velocityY += GRAVITY;


    if (playerY >= GROUND_HEIGHT + 10) {
        playerY = GROUND_HEIGHT + 10;
        isJumping = 0;
    }
    
    if (maps[level][playerY / (SCREEN_HEIGHT / MAP_HEIGHT)][playerX / (SCREEN_WIDTH / MAP_WIDTH)] == 5) {
        level = (level + 1) % 2;
        playerX = 100;
        playerY = GROUND_HEIGHT + 10;
    }
    
    // Cek tabrakan dengan musuh, duri, coin, starpower, nextLevel,Block
    cheakCollisionWithBlock();
    checkCollisionWithMonster();
    checkCollisionWithSpike();
    checkCollisionWithCoin();
    checkCollisionWithStar();
    checkCollisionWithNextLevel();

    // Gerakan musuh
    monsterX += monsterDirection * 2; // Kecepatan gerak musuh

    // Batasi gerakan bolak-balik
    if (monsterX <= 200 || monsterX >= 500) {
        monsterDirection *= -1; // Ubah arah jika mencapai batas
    }

    // Untuk Star Power
    if (hasStarPower) {
        starPowerTimer--;
        if (starPowerTimer <= 0) {
            hasStarPower = 0;  // Matikan Star Power setelah 10 detik
        }
    }
    

}

